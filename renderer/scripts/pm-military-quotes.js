function pickRandomPmQuote() {
  if (!PM_MILITARY_QUOTES.length) return { text: '', author: '' };
  const i = Math.floor(Math.random() * PM_MILITARY_QUOTES.length);
  return PM_MILITARY_QUOTES[i];
}

function renderRandomPmQuote() {
  const el = document.getElementById('pm-quote');
  if (!el) return;
  const q = pickRandomPmQuote();
  el.textContent = `「${q.text}」— ${q.author}`;
}

window.renderRandomPmQuote = renderRandomPmQuote;
window.PM_MILITARY_QUOTES = PM_MILITARY_QUOTES;
